/* How could you turn the array [3, 2, 1] into the string
"3 is bigger than 2 is bigger than 1" using the join method? */

// ✅SOLUTION✅

const arr = [3, 2, 1]
const newString = arr.join(" is bigger than ")
console.log(newString) // 3 is bigger than 2 is bigger than 1

